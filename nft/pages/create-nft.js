import {
  marketplaceAddress, Web3Storage_token
} from '../config'

// use web3 storage: https://web3.storage/docs/reference/js-client-library/?js-lib=browser
import { Web3Storage } from 'web3.storage'

// Construct with token and endpoint
const client = new Web3Storage({ token: Web3Storage_token });

import NFTMarketplace from '../artifacts/contracts/NFTMarketplace.sol/NFTMarketplace.json'
import { constructImgUrl } from "../utils/image_url_helper";

function isFileImage(file) {
  return file && file['type'].split('/')[0] === 'image';
}

export default function CreateItem() {
  const [fileUrl, setFileUrl] = useState(null)
  const [formInput, updateFormInput] = useState({ price: '0.01', name: 'e.g., 大碗岛的星期天下午', 
  description: `e.g., 作品名:大碗岛的星期天下午 A Sunday Afternoon on the Ile de la Grande Jatte
  艺术家：乔治·修拉
  创作时间：1884－1886年
  作品材质:布面油画 207.5x308.1cm
  现藏地点:美国-芝加哥-芝加哥艺术学院`, guid: '作品的MD5唯一标识符，由上传文件自动生成' })
  const router = useRouter()

  async function onChange(e) {
    const file = e.target.files[0]
    var reader = new FileReader();
    const ext = file.name.split('.').pop()

    console.log(isFileImage(file))

    reader.onload = function(event) {
      var binary = event.target.result;
      var md5 = CryptoJS.MD5(binary).toString();
      updateFormInput({ ...formInput, guid: md5 + '.' + ext })
    };
    reader.readAsBinaryString(file); // get MD5 hash

    var reader = new FileReader();
    reader.onload = function(event) {
      setFileUrl(event.target.result)
    };
    reader.readAsDataURL(file); // get base64 for image display
  }

  async function listNFTForSale() {
    const url = await uploadToIPFS()
    const web3Modal = new Web3Modal()
    const connection = await web3Modal.connect()
    const provider = new ethers.providers.Web3Provider(connection)
    const signer = provider.getSigner()

    /* next, create the item */
    const price = ethers.utils.parseUnits(formInput.price, 'ether')
    let contract = new ethers.Contract(marketplaceAddress, NFTMarketplace.abi, signer)
    let listingPrice = await contract.getListingPrice()
    listingPrice = listingPrice.toString()
    let transaction = await contract.createToken(url, price, { value: listingPrice })
    await transaction.wait()
   
    router.push('/')
  }

  async function uploadToIPFS() {
    const { name, description, price, guid } = formInput
    if (!name || !description || !price || !guid || !fileUrl) return

    /* first, upload to IPFS */
    const json_data = JSON.stringify({
      name, description, price, guid
    })

    const blob = new Blob([json_data], { type: 'application/json' })
    const file = dataURLtoFile(fileUrl, guid) // revert the img src to a file object
    
    // const fileInput = document.querySelector('input[type="file"]')

    // Pack files into a CAR and send to web3.storage
    const rootCid = await client.put([
      file, 
      new File([blob], guid + '.json')]) // fileInput.files

    // Get info on the Filecoin deals that the CID is stored in
    const info = await client.status(rootCid) // Promise<Status | undefined>

    // Fetch and verify files from web3.storage
    const res = await client.get(rootCid) // Promise<Web3Response | null>
    const files = await res.files() // Promise<Web3File[]>

    for (const file of files) {
      // console.log(`${file.cid} ${file.name} ${file.size}`)
      const url = `https://${rootCid.toString()}.ipfs.w3s.link/${file.name}`
      if (!url.endsWith('.json')) {
        return url; //setFileUrl(url)
      }
    }
  }

  return (
    <div className="flex justify-center">
      <div className="w-1/2 flex flex-col pb-12">
        <input 
          placeholder="Asset Name"
          className="mt-8 border rounded p-4"
          value = {formInput.name}
          onChange={e => updateFormInput({ ...formInput, name: e.target.value })}
        />
        <textarea
          placeholder="Asset Description"
          className="mt-2 border rounded p-4 h-36"
          value={formInput.description}
          onChange={e => updateFormInput({ ...formInput, description: e.target.value })}
        />
        <input
          placeholder="Asset Price in MATIC"
          className="mt-2 border rounded p-4"
          value = {formInput.price}
          onChange={e => updateFormInput({ ...formInput, price: e.target.value })}
        />
        <input
          type='display'
          placeholder="Input Assert GUID/DIGEST/HASH"
          className="mt-2 border rounded p-4"
          value = {formInput.guid}
          readOnly = {true}
        />
        <input
          type="file"
          name="Asset"
          className="my-4"
          accept="image/*"
          onChange={onChange}
        />
        {
          fileUrl && (
            <img className="rounded mt-4" width="350" src={fileUrl} />
          )
        }
        <button onClick={listNFTForSale} className="font-bold mt-4 bg-pink-500 text-white rounded p-4 shadow-lg">
          Create NFT
        </button>
      </div>
    </div>
  )
}